__all__ = ["addition", "subtraction", "multiplication", "division"]

def addition(num1: float, num2: float) -> float:
    """This function returns the sum of two given numbers"""
    return num1 + num2

def subtraction(num1: float, num2: float) -> float:
    """This function returns the subtraction of two given numbers"""
    return num1 - num2

def multiplication(num1: float, num2: float) -> float:
    """This function returns the multiplication of two given numbers"""
    return num1 * num2

def division(num1: float, num2: float) -> float:
    """This function returns the division of two given numbers"""
    return num1 / num2
