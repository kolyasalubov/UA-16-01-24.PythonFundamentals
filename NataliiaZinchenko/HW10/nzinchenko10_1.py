# Creating a polygon class and a rectangle class and finding square of rectangle

class Polygon():
    '''
    Class defining Polygon objects
    '''
    def __init__(self, square = 0, area = 0):
        self.area = area
        self.square = square



class Rectangle():
    '''
    Class defining Rectangle objects
    '''
    def __init__(self, width = 0, height = 0):
        self.width = width
        self.height = height